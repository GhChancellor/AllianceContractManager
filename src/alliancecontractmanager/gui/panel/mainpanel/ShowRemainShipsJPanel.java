    /**
     * Write Value To jTable in ShowRemainShipsJPanel
     * @param 
     */
    private void writeValueToTable( Map < String, RemainingShips > doctrineMap, int index  ){
        switch (index){
            case 0:
//                writeValueToTableOutstanding(doctrineMap);
            case 1:
                writeValueToTableOutstanding(doctrineMap);
                break;
        }
    }

    private void writeValueToTableOutstanding( Map < String, RemainingShips > doctrineMap){
        showRemainjTableOutstadingModel1.clear();
        
        for (Map.Entry<String, RemainingShips> entry : doctrineMap.entrySet()) {
            showRemainjTableOutstadingModel1.addContract(entry.getValue());
        }
    }
    
