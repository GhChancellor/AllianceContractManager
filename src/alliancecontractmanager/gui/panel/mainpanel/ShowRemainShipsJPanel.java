    private void jComboBoxStatusContract02ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxStatusContract02ActionPerformed
        int selectedItem = this.jComboBoxStatusContract02.getSelectedIndex();
        
        switch (selectedItem){
            case 0:
                Map < String, RemainingShips > doctrineCompletedMap =
                 ManagerRemainingShips.getInstance().getDoctrineCompletedMap();
                writeValueToTable(doctrineCompletedMap, selectedItem);
                break;
            case 1:
                Map < String, RemainingShips > doctrineOutstandingMap =
                 ManagerRemainingShips.getInstance().getDoctrineOutstandingMap(); 
                writeValueToTable(doctrineOutstandingMap, selectedItem);
                break;
        }        
    }//GEN-LAST:event_jComboBoxStatusContract02ActionPerformed

